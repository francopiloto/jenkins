package ifce.polo.sippi.util;

public class UID
{
    private Long id;
    private String table;

/* --------------------------------------------------------------------------------------------- */

    public UID(Long id, String table)
    {
        this.id = id;
        this.table = table;
    }

/* --------------------------------------------------------------------------------------------- */

    public Long getId() {
        return id;
    }

/* --------------------------------------------------------------------------------------------- */

    public String getTable() {
        return table;
    }

/* --------------------------------------------------------------------------------------------- */

}
